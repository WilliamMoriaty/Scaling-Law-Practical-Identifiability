function yy=poly_para(x,theta1,theta2,theta3,theta4)
H = Hessian_matrix();
yy=zeros(1,4);
yy(1) = (abs(x-2.5)-3) + H(1,1)*theta1*(x-1)*(x-2)*(x-3)/((4-1)*(4-2)*(4-3));
yy(2) = ((x-1)*(x-2)*(x-3)*(x-4)+1.5) + H(2,2)*theta3*(x-1)*(x-3)*(x-4)/((2-1)*(2-3)*(2-4));
yy(3) = ((x-1)*(x-2)*(x-3)*(x-4)+1.0) + H(3,3)*theta2*(x-2)*(x-3)*(x-4)/((1-2)*(1-3)*(1-4));
yy(4) = ((x-1)*(x-2)*(x-3)*(x-4)+0.5) + H(4,4)*theta4*(x-1)*(x-2)*(x-4)/((3-1)*(3-2)*(3-4));
end

