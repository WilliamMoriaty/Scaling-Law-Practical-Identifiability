function y = poly_function(x, theta1,theta2,theta3,theta4)

H = Hessian_matrix();
 y = theta2*((x-1)*(x-2)*(x-3)*(x-4)+1.0) + theta3*((x-1)*(x-2)*(x-3)*(x-4)+1.5)...
     + theta4*((x-1)*(x-2)*(x-3)*(x-4)+0.5) + theta1*(abs(x-2.5)-3) ...
     + 0.5*theta2*H(2,2)*theta2*(x-2)*(x-3)*(x-4)/((1-2)*(1-3)*(1-4))...
     + 0.5*theta4*H(4,4)*theta4*(x-1)*(x-2)*(x-4)/((3-1)*(3-2)*(3-4))...
     + 0.5*theta3*H(3,3)*theta3*(x-1)*(x-3)*(x-4)/((2-1)*(2-3)*(2-4))...
     + 0.5*theta1*H(1,1)*theta1*(x-1)*(x-2)*(x-3)/((4-1)*(4-2)*(4-3));
end